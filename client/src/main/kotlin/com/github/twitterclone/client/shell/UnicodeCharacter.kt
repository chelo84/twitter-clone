package com.github.twitterclone.client.shell

enum class UnicodeCharacter(val char: Char) {
    ARROW_LEFT('\u2190'), // ←
    ARROW_UP('\u2191'), // ↑
    ARROW_RIGHT('\u2192'), // →
    ARROW_DOWN('\u2193'), // ↓
    ARROW_UP_TO_LEFT('\u21B0'), // ↰
    ARROW_UP_TO_RIGHT('\u21B1'), // ↱
    ARROW_DOWN_TO_LEFT('\u21B2'), // ↲
    ARROW_DOWN_TO_RIGHT('\u21B3'), // ↳
}